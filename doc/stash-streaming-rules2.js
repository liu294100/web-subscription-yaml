function main(config) {
  // 1. Ensure config structures exist
  if (!config["proxy-groups"]) config["proxy-groups"] = [];
  if (!config["rule-providers"]) config["rule-providers"] = {};
  if (!config["rules"]) config["rules"] = [];

  // 2. Add Rule Providers
  RULE_SETS.forEach(set => {
    if (set.type === 'provider') {
      const providerName = 'Provider_' + CryptoJS.MD5(set.group + set.url).toString().substring(0, 8);
      set.providerName = providerName; // Save for rule generation
      
      config["rule-providers"][providerName] = {
        type: "http",
        behavior: set.behavior,
        url: set.url,
        path: `./ruleset/${set.group.replace(/[^a-zA-Z0-9]/g, '')}_${providerName}.yaml`,
        interval: 86400
      };
    }
  });

  // 3. Add/Update Proxy Groups
  // We need to insert them. To preserve order, we can append them or insert at top.
  // Given the INI defines the hierarchy, we should probably add them all.
  // However, existing groups might conflict. We will overwrite or append if missing.
  
  const existingGroupNames = new Set(config["proxy-groups"].map(g => g.name));
  
  // We process in reverse order to ensure dependencies if we unshift? 
  // Or just push? The INI has dependencies (Group A uses Group B).
  // Stash resolves names lazily so order in array mostly affects UI.
  
  PROXY_GROUPS.forEach(groupDef => {
    // Construct Stash Group Object
    const stashGroup = {
      name: groupDef.name,
      type: groupDef.type,
      proxies: groupDef.proxies || []
    };
    
    // Add regex filter if present
    if (groupDef.filter) {
        // Remove enclosing ` or just use string
        // The parser likely kept it clean
        // Stash uses 'include-all-proxies': true/false + 'filter': 'regex'
        // If the regex is ".*", it implies all proxies.
        
        // If regex is present, we often want to include all proxies that match
        stashGroup['include-all-proxies'] = true;
        stashGroup['filter'] = groupDef.filter;
    }
    
    // Add url/interval for auto groups
    if (['url-test', 'fallback', 'load-balance'].includes(groupDef.type)) {
        stashGroup.url = groupDef.url;
        stashGroup.interval = groupDef.interval;
    }
    
    // If group exists, update it? Or skip?
    // The requirement is to "write one", implying override.
    const idx = config["proxy-groups"].findIndex(g => g.name === groupDef.name);
    if (idx !== -1) {
        // Update existing
        // Merge proxies? Or Replace?
        // Let's replace properties but keep existing proxies if not specified?
        // INI specifies full list. Let's replace.
        config["proxy-groups"][idx] = stashGroup;
    } else {
        config["proxy-groups"].push(stashGroup);
    }
  });

  // 4. Add Rules
  // Rules need to be added in order. 
  // We should prepend them to ensure they take precedence, or append?
  // Usually "GeneralClashRule" implies a full set.
  // We will Prepend.
  
  const newRules = [];
  
  RULE_SETS.forEach(set => {
    if (set.type === 'provider') {
      newRules.push(`RULE-SET,${set.providerName},${set.group}`);
    } else if (set.type === 'geoip') {
      newRules.push(`GEOIP,${set.value},${set.group}`);
    } else if (set.type === 'final') {
      newRules.push(`MATCH,${set.group}`);
    }
  });
  
  // Prepend new rules
  config["rules"] = [...newRules, ...config["rules"]];

  return config;
}
